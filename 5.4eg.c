//了解自增运算符和自减运算符
// Created by 仇皓然 on 24-10-15.
#include<stdio.h>
int main(){
    int a = 10;
    printf("%d\n",a++);
    printf("%d\n",a);

    a = 10;
    printf("%d\n",++a);
    printf("%d\n",a);

    a = 10;
    printf("%d\n",--a);
    printf("%d\n",a);

    a = 10;
    printf("%d\n",a--);
    printf("%d\n",a);
}